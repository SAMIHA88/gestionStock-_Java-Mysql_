     private void loadCommande() {
        model.setRowCount(0);
        for (Commande cm : cms.findAll()) {
            model.addRow(new Object[]{
               cm.getCode(),
                cm.getDate(),
                cm.getClient()
            });
        }
    }

    private void loadClient() {
        for (Client c : cs.findAll()) {
            clientList.addItem(c);
        }
    }
