    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
        // TODO add your handling code here:
        Date date = txtDate.getDate();
        Client client = (Client) clientList.getSelectedItem();
        if (cms.create(new Commande(date,client))) {
            JOptionPane.showMessageDialog(this, "Bien ajouté");
            loadCommande();
        }
        
    }//GEN-LAST:event_btnAjouterActionPerformed

    private void btnModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifierActionPerformed
        // TODO add your handling code here:
        
        Date date = txtDate.getDate();
        Client client = (Client) clientList.getSelectedItem();
        if(cms.update(new Commande(code,date,client))){
            JOptionPane.showMessageDialog(this, "Bien modifié");
            loadCommande();
    }//GEN-LAST:event_btnModifierActionPerformed
    }
    private void commandeListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_commandeListMouseClicked
        // TODO add your handling code here:
        code=Integer.parseInt( model.getValueAt(commandeList.getSelectedRow(),0).toString());
       
          txtDate.setDate((Date)model.getValueAt(commandeList.getSelectedRow(),1));
          clientList.setSelectedItem((Client)model.getValueAt(commandeList.getSelectedRow(),2));
          
    }//GEN-LAST:event_commandeListMouseClicked

    private void btnSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupprimerActionPerformed
        // TODO add your handling code here:
           if(code != 0){
            int reponse = JOptionPane.showConfirmDialog(this, "Voulez vous vraiment supprimer cette commande ? ");
            if(reponse == 0){
                cms.delete(cms.findById(code));
                loadCommande();
                JOptionPane.showMessageDialog(this, "Bien supprimé");
            }
      }
    }//GEN-LAST:event_btnSupprimerActionPerformed


