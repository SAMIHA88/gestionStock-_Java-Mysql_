    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
        // TODO add your handling code here:
             
        String code  = txtCode.getText();
        if(rs.create(new Rayon(code))){
            JOptionPane.showMessageDialog(this, "Bien ajouté");
            load();
        }
    }//GEN-LAST:event_btnAjouterActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
       if(id != 0){
            int reponse = JOptionPane.showConfirmDialog(this, "Voulez vous vraiment supprimer ce rayon ? ");
            if(reponse == 0){
                rs.delete(rs.findById(id));
                JOptionPane.showMessageDialog(this, "Bien supprimé");
                  load();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifyActionPerformed
        // TODO add your handling code here:
         String code  = txtCode.getText();
        if(rs.update(new Rayon(id,  code))){
            JOptionPane.showMessageDialog(this, "Bien modifier");
            load();
    }              
    }//GEN-LAST:event_btnModifyActionPerformed

